def get_random_safe_emoji() -> str:
    """Get a random safe emoji."""
    return random.choice(SAFE_EMOJIS)


def get_random_safe_emojis(count: int) -> list[str]:
    """Get multiple unique random safe emojis."""
    return random.sample(SAFE_EMOJIS, min(count, len(SAFE_EMOJIS)))


def emoji_to_decimal(emoji: str) -> str:
    """Convert an emoji to its decimal Unicode code point.

    For emojis that consist of multiple code points (like flags or
    skin tone variants), this returns the first code point.
    """
    return str(ord(emoji[0]))


def get_random_logo_props() -> dict:
    """Get a random emoji formatted as logo_props for a project."""
    emoji = get_random_safe_emoji()
    return {
        "emoji": {
            "value": emoji_to_decimal(emoji)
        },
        "in_use": "emoji"
    }
