/**
 * Get a random safe emoji
 */
export function getRandomSafeEmoji(): string {
  const randomIndex = Math.floor(Math.random() * SAFE_EMOJIS.length);
  return SAFE_EMOJIS[randomIndex];
}

/**
 * Get multiple unique random safe emojis
 */
export function getRandomSafeEmojis(count: number): string[] {
  const shuffled = [...SAFE_EMOJIS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, SAFE_EMOJIS.length));
}
